"""Word report generation using tesorotools render.

Builds a Word document that mirrors the structure of the
original hogares report: title, chart sections organized
in rows of 1-2 images each, with titles and source notes.

Charts are referenced by ID (matching the PNG filenames
generated by ``src.charts``).  Missing charts are skipped
gracefully so the report can be generated with partial
data.
"""

import logging
from pathlib import Path
from typing import Any

import yaml
from docx import Document

from tesorotools.render import (  # pyright: ignore[reportMissingTypeStubs]
    Image,
    Images,
    Report,
    Section,
    Text,
    Title,
)

logger = logging.getLogger(__name__)


# ----------------------------------------------------------
